package holocaustoH;

public class Posicion {
	//properties
	private int posX;
	private int posY;
	
	//setters && getters
	public int getPosX() {
		return posX;
	}
	public void setPosX(int posX) {
		this.posX = posX;
	}
	public int getPosY() {
		return posY;
	}
	public void setPosY(int posY) {
		this.posY = posY;
	}
	
}
